import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int A, B, C;
        A = scan.nextInt();
        B = scan.nextInt();
        C = scan.nextInt();
        
        scan.close();
        
        String myString = A + "\n" + B + "\n" + C;
        System.out.println(myString);
    }
}
